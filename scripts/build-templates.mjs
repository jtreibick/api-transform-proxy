import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const templatesDir = path.join(repoRoot, 'src', 'ui', 'templates');
const outFile = path.join(repoRoot, 'src', 'ui', 'templates.js');

function toTemplateKey(fileName) {
  if (fileName.toLowerCase().endsWith(".js")) return fileName.replace(/\.js$/i, "_js");
  return fileName.replace(/\.html$/i, "");
}

async function main() {
  const files = (await readdir(templatesDir))
    .filter((f) => /\.(html|js)$/i.test(f))
    .sort();

  const entries = [];
  for (const fileName of files) {
    const abs = path.join(templatesDir, fileName);
    const content = await readFile(abs, 'utf8');
    entries.push(`  ${JSON.stringify(toTemplateKey(fileName))}: ${JSON.stringify(content)}`);
  }

  const generated = `// Auto-generated by scripts/build-templates.mjs\n// Do not edit by hand. Edit src/ui/templates/*.{html,js} and rebuild.\nexport const templates = {\n${entries.join(',\n')}\n};\n`;
  await writeFile(outFile, generated, 'utf8');
  process.stdout.write(`Built ${files.length} templates -> ${path.relative(repoRoot, outFile)}\n`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
